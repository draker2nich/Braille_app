#pragma once

#include <Arduino.h>

/**
 * Брайль: 6 точек на ячейку. Кодируется строкой из 6 символов '0'/'1'.
 *
 * Формат токена: <PREFIX>_<6 BITS>, где PREFIX = 'R' для русского, 'E' для английского.
 * Пример: "R_111000" "E_100100"
 *
 * Назначение этого модуля минимальное: распарсить входящий пакет токенов
 * во внутреннее представление (массив битмасок 6 бит) и предоставить
 * человекочитаемую расшифровку для отладки.
 *
 * Декодирование "битмаска -> буква" нужно только для логов — устройство
 * физически отображает биты напрямую, ему всё равно, какая это буква.
 * Таблицы вынесены в .cpp чтобы не таскать килобайт PROGMEM туда, где не надо.
 */

namespace braille {

constexpr uint8_t DOTS = 6;

/// Одна Брайль-ячейка: 6 бит, бит i = точка i+1.
struct Cell {
    uint8_t bits = 0;            // младшие 6 бит используются
    char    lang = 'E';          // 'R' / 'E' — для возможной диагностики
};

/// Распарсить один токен "R_111000" или "E_100100". Возвращает true при успехе.
bool parseToken(const char* token, size_t len, Cell& out);

/// Заполнить массив bool[6] из ячейки для StepperBank::setPattern().
inline void cellToDots(const Cell& c, bool dots[DOTS]) {
    for (uint8_t i = 0; i < DOTS; ++i) {
        dots[i] = (c.bits >> i) & 1;
    }
}

/// Опционально: расшифровать ячейку в букву (для отладки/уведомлений).
/// Возвращает указатель на статическую UTF-8 строку или "?".
const char* decode(const Cell& c);

} // namespace braille
